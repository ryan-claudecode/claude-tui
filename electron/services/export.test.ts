import { describe, it, expect, beforeEach, afterEach } from "vitest"
import { mkdtempSync, rmSync, mkdirSync, writeFileSync, readFileSync, existsSync, statSync } from "node:fs"
import { tmpdir } from "node:os"
import { join } from "node:path"
import { ExportService, type ExportDeps, GITIGNORE_ENTRY } from "./export"
import { buildWorkspacePrimerBody, orderWorkspaceFindings, type InjectWorkspaceFinding } from "./contextInject"
import { UNTAGGED_STEM } from "./workspaceMemory"

/**
 * CAPP-99 / E1 — ExportService + the shared workspace-primer body.
 *
 * Hermetic: a temp registry dir + temp "workspace folders". NEVER touches a real repo.
 * Pins the load-bearing safety invariants a reviewer will probe: the §B.1 format + identity
 * marker; refuse-to-overwrite a marker-less file; gitignore-FIRST ordering (entry before the
 * file; decline → no export; idempotent re-run; CRLF preserved; content kept); atomic
 * temp-then-rename + only-rewrite-if-changed; live regen; untagged → Mode C only + default-OFF;
 * the ONE-DIRECTIONAL guarantee (editing the export file does NOT change the store); regen-on-launch.
 */

let root: string
let folderA: string

/** A deps stub backed by a mutable in-memory store, so a test can mutate the "store" and
 *  assert the export reflects it — and assert the reverse never happens (one-directional). */
function makeDeps(store: {
  folders: Record<string, string | null>
  instructions: Record<string, string>
  findings: Record<string, InjectWorkspaceFinding[]>
}): ExportDeps {
  return {
    resolveFolder: (id) => store.folders[id] ?? null,
    getInstructions: (id) => store.instructions[id ?? UNTAGGED_STEM] ?? "",
    workspaceFindings: (id) => store.findings[id ?? UNTAGGED_STEM] ?? [],
  }
}

beforeEach(() => {
  root = mkdtempSync(join(tmpdir(), "export-test-"))
  folderA = join(root, "repo-a")
  mkdirSync(folderA, { recursive: true })
})

afterEach(() => {
  rmSync(root, { recursive: true, force: true })
})

describe("buildWorkspacePrimerBody (shared, anti-drift)", () => {
  it("orders pinned-first, then active oldest-first, then ruled-out", () => {
    const findings: InjectWorkspaceFinding[] = [
      { text: "newer active", status: "active", createdAt: 200 },
      { text: "older active", status: "active", createdAt: 100 },
      { text: "ruled out", status: "ruled-out", createdAt: 50, correction: "use X" },
      { text: "pinned one", status: "active", createdAt: 300, pinned: true },
    ]
    const ordered = orderWorkspaceFindings(findings)
    expect(ordered.map((f) => f.text)).toEqual([
      "pinned one",
      "older active",
      "newer active",
      "ruled out",
    ])
  })

  it("renders the §B markdown body (pin marker, strikethrough+correction)", () => {
    const body = buildWorkspacePrimerBody({
      instructions: "Always use TypeScript.",
      workspaceFindings: [
        { text: "Pinned rule", status: "active", createdAt: 1, pinned: true },
        { text: "Active fact", status: "active", createdAt: 2 },
        { text: "Bad idea", status: "ruled-out", createdAt: 3, correction: "do Y instead" },
      ],
    })
    expect(body).toContain("## Standing instructions\nAlways use TypeScript.")
    expect(body).toContain("- 📌 Pinned rule")
    expect(body).toContain("- Active fact")
    expect(body).toContain("- ~~Bad idea~~ → do Y instead")
  })

  it("returns empty when nothing durable (no instructions, no findings)", () => {
    expect(buildWorkspacePrimerBody({ instructions: "  ", workspaceFindings: [] })).toBe("")
  })
})

describe("ExportService — format + identity marker", () => {
  it("buildFileContent emits the exact §B.1 header + marker + provenance line", () => {
    const store = {
      folders: { "ws-1": folderA },
      instructions: { "ws-1": "Hello." },
      findings: { "ws-1": [{ text: "f1", status: "active" as const, createdAt: 1 }] },
    }
    const svc = new ExportService(makeDeps(store), { registryDir: root })
    const content = svc.buildFileContent("ws-1")
    expect(content.split("\n")[0]).toBe(
      "<!-- mission-control:workspace-memory v1 workspace=ws-1 -->",
    )
    expect(content).toContain("AUTO-GENERATED by Mission Control")
    expect(content).toContain("edits here are NOT read back")
    expect(content).toContain("# Workspace memory")
    expect(content).toContain("## Standing instructions\nHello.")
    expect(content).toContain("- f1")
  })

  it("the untagged file carries the sentinel stem in its marker", () => {
    const svc = new ExportService(makeDeps({ folders: {}, instructions: {}, findings: {} }), {
      registryDir: root,
    })
    expect(svc.buildFileContent(null).split("\n")[0]).toBe(
      `<!-- mission-control:workspace-memory v1 workspace=${UNTAGGED_STEM} -->`,
    )
  })
})

describe("ExportService — Mode A gitignore-first", () => {
  function svcFor(findings: InjectWorkspaceFinding[] = [{ text: "f", status: "active", createdAt: 1 }]) {
    const store = {
      folders: { "ws-1": folderA },
      instructions: { "ws-1": "Instr." },
      findings: { "ws-1": findings },
    }
    return new ExportService(makeDeps(store), { registryDir: root })
  }

  it("writes the /.claude-tui/ entry BEFORE the export file lands", () => {
    const svc = svcFor()
    const res = svc.enableExport("ws-1", "A")
    expect(res.ok).toBe(true)
    const gi = readFileSync(join(folderA, ".gitignore"), "utf8")
    expect(gi.split(/\r?\n/)).toContain(GITIGNORE_ENTRY)
    expect(existsSync(join(folderA, ".claude-tui", "workspace-memory.md"))).toBe(true)
  })

  it("declining the gitignore write (read-only) → NO export, NO untracked file", () => {
    // Simulate a gitignore that cannot be written: make .gitignore a DIRECTORY so writeFileSync EISDIRs.
    mkdirSync(join(folderA, ".gitignore"))
    const svc = svcFor()
    const res = svc.enableExport("ws-1", "A")
    expect(res.ok).toBe(false)
    expect(res.error).toMatch(/gitignore/i)
    // No registry entry, and no export file left untracked.
    expect(svc.getExportState("ws-1").enabled).toBe(false)
    expect(existsSync(join(folderA, ".claude-tui", "workspace-memory.md"))).toBe(false)
  })

  it("is idempotent — a second enable doesn't duplicate the gitignore entry", () => {
    const svc = svcFor()
    svc.enableExport("ws-1", "A")
    svc.enableExport("ws-1", "A")
    const lines = readFileSync(join(folderA, ".gitignore"), "utf8").split(/\r?\n/)
    expect(lines.filter((l) => l === GITIGNORE_ENTRY)).toHaveLength(1)
  })

  it("preserves existing CRLF gitignore content + EOL, appends without clobbering", () => {
    // A CRLF file with prior content and no trailing newline.
    writeFileSync(join(folderA, ".gitignore"), "node_modules\r\ndist", "utf8")
    const svc = svcFor()
    svc.enableExport("ws-1", "A")
    const gi = readFileSync(join(folderA, ".gitignore"), "utf8")
    expect(gi).toContain("node_modules")
    expect(gi).toContain("dist")
    expect(gi.split(/\r?\n/)).toContain(GITIGNORE_ENTRY)
    // CRLF preserved (the appended entry uses the file's detected EOL).
    expect(gi.includes("\r\n")).toBe(true)
  })

  it("does NOT add a near-duplicate when the user already ignored .claude-tui/", () => {
    writeFileSync(join(folderA, ".gitignore"), ".claude-tui/\n", "utf8")
    const svc = svcFor()
    svc.enableExport("ws-1", "A")
    const gi = readFileSync(join(folderA, ".gitignore"), "utf8")
    expect(gi.split(/\r?\n/).filter((l) => l.includes(".claude-tui"))).toHaveLength(1)
  })

  it("regen SKIPS (doesn't re-create untracked) when the gitignore entry is removed", () => {
    const svc = svcFor()
    svc.enableExport("ws-1", "A")
    // User removes the .gitignore entry + deletes the file.
    writeFileSync(join(folderA, ".gitignore"), "node_modules\n", "utf8")
    rmSync(join(folderA, ".claude-tui", "workspace-memory.md"))
    const r = svc.regenerate("ws-1")
    expect(r.ok).toBe(false)
    expect(r.error).toMatch(/gitignore/i)
    expect(existsSync(join(folderA, ".claude-tui", "workspace-memory.md"))).toBe(false)
  })
})

describe("ExportService — refuse to overwrite a foreign file", () => {
  it("throws (caught → regen ok:false) when the dest exists but lacks our marker", () => {
    const dest = join(folderA, ".claude-tui", "workspace-memory.md")
    mkdirSync(join(folderA, ".claude-tui"), { recursive: true })
    writeFileSync(dest, "# My own hand-authored notes\nDo not delete.\n", "utf8")
    const store = {
      folders: { "ws-1": folderA },
      instructions: { "ws-1": "x" },
      findings: { "ws-1": [] },
    }
    const svc = new ExportService(makeDeps(store), { registryDir: root })
    // Pre-seed the gitignore so the gitignore-first guard passes; the marker guard must still refuse.
    writeFileSync(join(folderA, ".gitignore"), `${GITIGNORE_ENTRY}\n`, "utf8")
    const res = svc.enableExport("ws-1", "A")
    expect(res.ok).toBe(false)
    expect(res.error).toMatch(/identity marker|Mission Control export/i)
    // The user's file is untouched.
    expect(readFileSync(dest, "utf8")).toContain("Do not delete.")
  })

  it("fails CLOSED when the dest EXISTS but is unreadable — refuses, never stomps", () => {
    // existsSync(dest)===true but readFileSync throws (a directory at the path forces EISDIR —
    // a hermetic stand-in for a transiently-locked user file). The guard must REFUSE, not treat
    // it as absent and write over it.
    mkdirSync(join(folderA, ".claude-tui", "workspace-memory.md"), { recursive: true })
    writeFileSync(join(folderA, ".gitignore"), `${GITIGNORE_ENTRY}\n`, "utf8")
    const store = { folders: { "ws-1": folderA }, instructions: { "ws-1": "x" }, findings: { "ws-1": [] } }
    const svc = new ExportService(makeDeps(store), { registryDir: root })
    const res = svc.enableExport("ws-1", "A")
    expect(res.ok).toBe(false)
    expect(res.error).toMatch(/unreadable/i)
    // Still a directory — never stomped into our file.
    expect(statSync(join(folderA, ".claude-tui", "workspace-memory.md")).isDirectory()).toBe(true)
  })
})

describe("ExportService — gitignore rollback on a failed first regen", () => {
  /** A foreign (marker-less) file at the dest forces the first regen to refuse, exercising the
   *  enableExport rollback path. */
  function seedForeignDest(): void {
    mkdirSync(join(folderA, ".claude-tui"), { recursive: true })
    writeFileSync(join(folderA, ".claude-tui", "workspace-memory.md"), "# foreign, no marker\n", "utf8")
  }

  it("removes the /.claude-tui/ line WE added when the first regen fails", () => {
    seedForeignDest() // folderA has NO .gitignore yet → our enable will add the line
    const store = { folders: { "ws-1": folderA }, instructions: { "ws-1": "x" }, findings: { "ws-1": [] } }
    const svc = new ExportService(makeDeps(store), { registryDir: root })
    const res = svc.enableExport("ws-1", "A")
    expect(res.ok).toBe(false)
    // The line we added is rolled back — no residual ignore entry for an export that never landed.
    const giPath = join(folderA, ".gitignore")
    const gi = existsSync(giPath) ? readFileSync(giPath, "utf8") : ""
    expect(gi).not.toContain(GITIGNORE_ENTRY)
  })

  it("leaves a PRE-EXISTING /.claude-tui/ line intact when the first regen fails", () => {
    seedForeignDest()
    // The user already ignores the dir — our rollback must NOT strip their line.
    writeFileSync(join(folderA, ".gitignore"), `node_modules\n${GITIGNORE_ENTRY}\n`, "utf8")
    const store = { folders: { "ws-1": folderA }, instructions: { "ws-1": "x" }, findings: { "ws-1": [] } }
    const svc = new ExportService(makeDeps(store), { registryDir: root })
    const res = svc.enableExport("ws-1", "A")
    expect(res.ok).toBe(false)
    const gi = readFileSync(join(folderA, ".gitignore"), "utf8")
    expect(gi).toContain(GITIGNORE_ENTRY)
    expect(gi).toContain("node_modules")
  })
})

describe("ExportService — atomic + only-rewrite-if-changed", () => {
  it("does not rewrite when the body is unchanged (no churn)", () => {
    const store = {
      folders: { "ws-1": folderA },
      instructions: { "ws-1": "stable" },
      findings: { "ws-1": [{ text: "f", status: "active" as const, createdAt: 1 }] },
    }
    const svc = new ExportService(makeDeps(store), { registryDir: root })
    expect(svc.enableExport("ws-1", "A").ok).toBe(true)
    const dest = join(folderA, ".claude-tui", "workspace-memory.md")
    const mtime1 = statSync(dest).mtimeMs
    // Regen with no store change → no write.
    const r = svc.regenerate("ws-1")
    expect(r.wrote).toBe(false)
    expect(statSync(dest).mtimeMs).toBe(mtime1)
  })

  it("rewrites when the store changes", () => {
    const store = {
      folders: { "ws-1": folderA },
      instructions: { "ws-1": "v1" },
      findings: { "ws-1": [] },
    }
    const svc = new ExportService(makeDeps(store), { registryDir: root })
    svc.enableExport("ws-1", "A")
    store.instructions["ws-1"] = "v2 — updated"
    const r = svc.regenerate("ws-1")
    expect(r.wrote).toBe(true)
    const dest = join(folderA, ".claude-tui", "workspace-memory.md")
    expect(readFileSync(dest, "utf8")).toContain("v2 — updated")
  })

  it("leaves no .tmp files behind after a successful write", () => {
    const store = {
      folders: { "ws-1": folderA },
      instructions: { "ws-1": "x" },
      findings: { "ws-1": [] },
    }
    const svc = new ExportService(makeDeps(store), { registryDir: root })
    svc.enableExport("ws-1", "A")
    const dir = join(folderA, ".claude-tui")
    const stray = require("node:fs").readdirSync(dir).filter((f: string) => f.includes(".tmp"))
    expect(stray).toHaveLength(0)
  })
})

describe("ExportService — one-directional (store → file, never back)", () => {
  it("editing the export file does NOT change the store, and a regen overwrites the edit", () => {
    const store = {
      folders: { "ws-1": folderA },
      instructions: { "ws-1": "canonical" },
      findings: { "ws-1": [] },
    }
    const svc = new ExportService(makeDeps(store), { registryDir: root })
    svc.enableExport("ws-1", "A")
    const dest = join(folderA, ".claude-tui", "workspace-memory.md")

    // The user (or another agent) edits the export file — adding a "finding" of their own.
    const tampered = readFileSync(dest, "utf8") + "\n- INJECTED finding that must NEVER reach the store\n"
    writeFileSync(dest, tampered, "utf8")

    // The store is unchanged by that edit (there is no read-back path).
    expect(store.instructions["ws-1"]).toBe("canonical")
    expect(store.findings["ws-1"]).toHaveLength(0)

    // A regen re-projects the store, overwriting the tampered content (still our marker file).
    const r = svc.regenerate("ws-1")
    expect(r.wrote).toBe(true)
    expect(readFileSync(dest, "utf8")).not.toContain("INJECTED finding")
  })
})

describe("ExportService — Mode C + untagged", () => {
  it("Mode C with no folder uses the default path under the registry dir", () => {
    const svc = new ExportService(makeDeps({ folders: {}, instructions: { "ws-1": "x" }, findings: {} }), {
      registryDir: root,
    })
    const res = svc.enableExport("ws-1", "C")
    expect(res.ok).toBe(true)
    const expected = join(root, "exports", "ws-1", "workspace-memory.md")
    expect(res.state?.path).toBe(expected)
    expect(existsSync(expected)).toBe(true)
    // Mode C @import line is the absolute path.
    expect(res.state?.importLine).toBe(`@${expected}`)
  })

  it("untagged is Mode-C-only, default-OFF, with a machine-wide warning", () => {
    const svc = new ExportService(makeDeps({ folders: {}, instructions: {}, findings: {} }), {
      registryDir: root,
    })
    // Mode A is rejected for untagged.
    const aRes = svc.enableExport(null, "A")
    expect(aRes.ok).toBe(false)
    expect(aRes.error).toMatch(/folder|custom path/i)

    // Mode C registers but is DEFAULT-OFF (no file written yet).
    const cRes = svc.enableExport(null, "C")
    expect(cRes.ok).toBe(true)
    expect(cRes.state?.enabled).toBe(false)
    const expected = join(root, "exports", UNTAGGED_STEM, "workspace-memory.md")
    expect(existsSync(expected)).toBe(false)

    // The state carries the machine-wide warning + folderless flag.
    const state = svc.getExportState(null)
    expect(state.folderless).toBe(true)
    expect(state.untaggedWarning).toMatch(/every raw/i)

    // Explicitly enabling writes the file.
    const on = svc.setUntaggedEnabled(true)
    expect(on.enabled).toBe(true)
    expect(existsSync(expected)).toBe(true)
  })
})

describe("ExportService — regen-on-launch self-heal", () => {
  it("regenerateAll re-materializes an enabled export stale from while the app was closed", () => {
    const store = {
      folders: { "ws-1": folderA },
      instructions: { "ws-1": "fresh" },
      findings: { "ws-1": [] },
    }
    const svc = new ExportService(makeDeps(store), { registryDir: root })
    svc.enableExport("ws-1", "A")
    const dest = join(folderA, ".claude-tui", "workspace-memory.md")
    // Simulate the app being closed while the store changed + the file went stale/missing.
    rmSync(dest)
    store.instructions["ws-1"] = "changed while closed"

    // A NEW service instance reads the persisted registry (the registry survived) and self-heals.
    const svc2 = new ExportService(makeDeps(store), { registryDir: root })
    svc2.regenerateAll()
    expect(existsSync(dest)).toBe(true)
    expect(readFileSync(dest, "utf8")).toContain("changed while closed")
  })

  it("a disabled export is NOT regenerated by regenerateAll", () => {
    const store = {
      folders: { "ws-1": folderA },
      instructions: { "ws-1": "x" },
      findings: { "ws-1": [] },
    }
    const svc = new ExportService(makeDeps(store), { registryDir: root })
    svc.enableExport("ws-1", "A")
    svc.disableExport("ws-1")
    const dest = join(folderA, ".claude-tui", "workspace-memory.md")
    rmSync(dest)
    svc.regenerateAll()
    expect(existsSync(dest)).toBe(false)
  })
})

describe("ExportService — regen no-op safety", () => {
  it("regenerate on an un-exported workspace is a quiet ok no-op", () => {
    const svc = new ExportService(makeDeps({ folders: {}, instructions: {}, findings: {} }), {
      registryDir: root,
    })
    const r = svc.regenerate("ws-unknown")
    expect(r.ok).toBe(true)
    expect(r.wrote).toBe(false)
  })
})
